package model;

public class Sala {

    private int idSala;
    private int numSala;
    private int capSala;

    public int getIdSala() {
        return idSala;
    }

    public void setIdSala(int idSala) {
        this.idSala = idSala;
    }

    public int getNumSala() {
        return numSala;
    }

    public void setNumSala(int numSala) {
        this.numSala = numSala;
    }

    public int getCapSala() {
        return capSala;
    }

    public void setCapSala(int capSala) {
        this.capSala = capSala;
    }
}
