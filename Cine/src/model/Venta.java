package model;

public class Venta {
    private int idVta;
    private int idClie;
    private int idPel;
    private int totEnt;

    public int getIdVta() {
        return idVta;
    }

    public void setIdVta(int idVta) {
        this.idVta = idVta;
    }

    public int getIdClie() {
        return idClie;
    }

    public void setIdClie(int idClie) {
        this.idClie = idClie;
    }

    public int getIdPel() {
        return idPel;
    }

    public void setIdPel(int idPel) {
        this.idPel = idPel;
    }

    public int getTotEnt() {
        return totEnt;
    }

    public void setTotEnt(int totEnt) {
        this.totEnt = totEnt;
    }


    
}
